public class If01 {
	public static void main(String[] args) {
		int number = 7;
		if (number < 5) {
			System.out.println(number + " は5未満です");
		}
		if (number < 10) {
			System.out.println(number + " は10未満です");
		}
		if (number < 15) {
			System.out.println(number + " は15未満です");
		}
	}
}

// 実行結果
/*
7 は10未満です
7 は15未満です
*/ 